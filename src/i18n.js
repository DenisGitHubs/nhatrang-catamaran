export function detectLang() {
  const tgLang = window.Telegram?.WebApp?.initDataUnsafe?.user?.language_code;
  const lang = tgLang || navigator.language || 'en';
  return lang.toLowerCase().startsWith('ru') ? 'ru' : 'en';
}
